public class InClassOne
{
	public static void main (String [] args)
	{
		//initialize all needed ints
		int int_num1 = 5, int_num2 = 3, int_num3 = 0;
		

		int_num3 = int_num1 * int_num2;
		
		//Display int math
		System.out.println("The result of the int multiplication is...");
		System.out.println(int_num3);
		System.out.println("\n");
		
		//initialize all needed doubles
		double dbl_num1 = 8.0, dbl_num2 = 3.0, dbl_num3 = 0.0;
		
		dbl_num3 = dbl_num1 / dbl_num2;
		
		//display double math
		System.out.println("The result of the double division is...");
		System.out.println(dbl_num3);
	}
}